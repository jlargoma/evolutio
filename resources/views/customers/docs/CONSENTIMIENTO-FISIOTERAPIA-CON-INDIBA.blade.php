    salud de mi persona que pudiera afectar a los tratamientos que se me van a realizar.
    Asimismo decido, dentro de las opciones clínicas disponibles, dar mi conformidad, libre,
    voluntaria y consciente de los tratamientos que se me han informado.</strong></p>
