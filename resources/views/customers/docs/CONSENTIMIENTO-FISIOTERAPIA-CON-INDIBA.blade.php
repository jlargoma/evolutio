<h3>CONSENTIMIENTO FISIOTERAPIA</h3>
<p>El paciente adjuntado en dicho apartado autoriza al fisioterapeuta a aplicar el tratamiento que
  más se adecue a su patología con el fin de buscar la mejoría del paciente.</p>
<p>En nuestro centro de Fisioterapia contamos con equipos para mejorar el servicio y la mejora
  del paciente. Cito las contraindicaciones que pudieran existir para no realizarse su uso. En
  caso de no haberlas, autorizo al fisioterapeuta a realizarlas.</p>
<p><b>El tratamiento mediante radiofrecuencia INDIBA ACTIV</b>, indicada para tratamiento de
  lesiones osteoarticulares y musculoesqueléticas, tratamiento de suelo pélvico y fisioestética.
  Las contraindicaciones para no utilizarse este sistema son:</p>
<ul>
  <li>Marcapasos o implantes electrónicos</li>
  <li>Embarazo</li>
  <li>Heridas abiertas o quemaduras recientes</li>
  <li>Tromboflebitis</li>
  <li>Falta de sensibilidad o problema neurológico</li>
  <li>Alergia al níquel o cromo</li>
</ul>
<p>Después del tratamiento puede sentir una zona de enrojecimiento, siendo inocuo y remite en
  unas horas. Debe avisar al fisioterapeuta si la temperatura es elevada ya que podría quemar,
  siendo esta de carácter local, y será tratada con terapia médica de manera conservadora.
<p><b>En caso de realizarse un tratamiento de SUELO PÉLVICO</b>, cuyo tratamiento se realiza
  de manera intracavitaria en la vagina y en el ano, con el fin de conocer el estado funcional de
  los músculos del suelo pélvico.
<p>El paciente deberá avisar de cualquier implante eléctrico o marcapasos, sospechas de
  infección o de embarazo, hipertensión arterial o cualquier proceso que pueda contraindicar el
  tratamiento.
