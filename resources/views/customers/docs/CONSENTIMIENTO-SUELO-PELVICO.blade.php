<h2>INFORMACIÓN</h2>
La Fisioterapia Perineal es una disciplina terapéutica que permite evaluar y tratar disfunciones del
suelo pélvico (incontinencia urinaria y/o anal, dolor perineal, prolapsos, etc.) y otras de la esfera
sexual (dispareunias, vaginismo, etc.) y que especialmente acompañan a la mujer en el post parto, la
menopausia y al hombre después de la cirugía de próstata.
<h2>EL TRATAMIENTO</h2>
Al igual que otras especialidades en fisioterapia,<b> la fisioterapia perineal</b> utiliza como tratamiento la
<b>electroterapia, biofeedback, técnicas manuales, masaje perineal, cinesiterapia y técnicas
  comportamentales.</b> Muchos de estos procedimientos son intracavitarios, es decir intravaginales y/o
ano/rectales. Deberá ser realizado por fisioterapeutas especializados y con garantías máxima de
higiene, siendo los electrodos intracavitarios de uso individual. El tratamiento no garantiza la curación
del paciente.
<h2>RIESGOS</h2>
La mayoría de las técnicas empleadas en fisioterapia perineal no presentan efectos adversos. La
electroterapia/electroestimulación puede provocar una sensación de cosquilleo u hormigueo que
normalmente es bien tolerado por los pacientes. Ocasionalmente puede producir molestia o dolor por
fenómenos de hipersensibilidad o mal contacto del electrodo.
