de embarazo, hipertensión arterial o cualquier otro proceso que pueda contraindicar el tratamiento.
Demandará de forma verbal cuanta información necesite para entender correctamente su proceso
terapéutico. Podrá retirar su consentimiento en cualquier momento del tratamiento.
